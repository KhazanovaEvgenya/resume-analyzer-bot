from openai import OpenAI
from config import PROXY_API_KEY

client = OpenAI(
    api_key=PROXY_API_KEY,
    base_url="https://api.proxyapi.ru/openai/v1"
)


def analyze_resume(resume_text: str, vacancy_text: str) -> str:
    prompt = f"""
Ты — эксперт по карьерному консультированию, HR и Data Science / IT-резюме.

Пользователь прислал резюме и вакансию. Нужно оценить, насколько резюме подходит под вакансию, и дать конкретные советы по усилению.

РЕЗЮМЕ:
{resume_text}

ВАКАНСИЯ:
{vacancy_text}

Сделай ответ на русском языке в таком формате, не используя жирный шрифт или заголовки:

1. Общая оценка соответствия резюме вакансии от 0 до 100%
2. Краткий вывод: подходит / частично подходит / слабо подходит
3. Сильные стороны резюме
4. Слабые места относительно вакансии
5. Какие навыки из вакансии не отражены в резюме
6. Что конкретно добавить в резюме
7. Как переписать блок "Опыт" или "О себе" сильнее
8. Примеры 5 сильных bullet points для резюме
9. Какие ключевые слова добавить для ATS/HR-фильтров
10. Итоговая рекомендация: стоит ли откликаться и что написать в сопроводительном письме

Не пиши общие советы. Пиши конкретно под эту вакансию.
"""

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {
                "role": "system",
                "content": "Ты помогаешь улучшать резюме под конкретные вакансии."
            },
            {
                "role": "user",
                "content": prompt
            }
        ],
        temperature=0.4
    )

    return response.choices[0].message.content